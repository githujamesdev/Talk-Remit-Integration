package com.remitxpress.co.ke.main.configs;

import com.remitxpress.co.ke.main.services.JwtInterceptor;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;

/**
 *
 * @author jgithu
 */
@Configuration
public class WebConfig implements WebMvcConfigurer {

    @Autowired
    private JwtInterceptor jwtInterceptor;

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        registry.addInterceptor(jwtInterceptor)
                .addPathPatterns("/**")
                .excludePathPatterns("/getTransactionStatus","/initiateTransaction","/accountLookup","/getAccountBalance" ,"/process","/getExchangeRate", "/getCountries", "/getCurrencies", "/getDeliveryMethod", "/getDeliveryType"); // Exclude the process endpoint
    }
}
